<h3>Kati Baier Bein</h3> 
<p>Ich bin Gesundheitstrainerin und bewege Menschen. Und das am liebsten im frei.Raum. Für die Organisation, die Zusammenarbeit mit unseren Partnern und das Ambiente bin ich verantwortlich. Kreativ sein und dass meine Ideen von anderen umgesetzt werden, dafür brennt mein Feuer.</p>

<h3>Werner Bein</h3>
<p>Ich bin selbständiger Unternehmensberater, Moderator und Vortragender. Mit dem frei.Raum verbindet mich die Realisierung einer Idee - auch wenn diese nicht von mir war - von wagen ersten Vorstellungen, über praktische und handwerkliche Umsetzung bis zur Fertigstellung. Und mit der Fertigstellung die Entstehung eines fortlaufenden Prozesses zur Belebung und Weiterentwicklung des frei.Raum und seiner Möglichkeiten.
	Für die Gestaltung und Durchführung von Seminaren, Workshops, Veranstaltungen und Präsentationen brennt mein Feuer.</p>